    private void newLotStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_newLotStateChanged
        switch(method.getSelectedIndex()){
            case 0 -> {
                finalLot.setText("" + (Integer.parseInt(actualLot.getText()) + Integer.parseInt(newLot.getValue().toString())));
            }
            case 1 -> {
                finalLot.setText("" + (Integer.parseInt(actualLot.getText()) - Integer.parseInt(newLot.getValue().toString())));
            }
            default -> {
                finalLot.setText("" + (Integer.parseInt(newLot.getValue().toString())));
            }
        }
    }//GEN-LAST:event_newLotStateChanged

    private void codeKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_codeKeyPressed
        if(evt.getKeyCode() == 10){
            try {
                String lot = null;
                ResultSet query = articleDB.getByCode(code.getText());
                
                while(query.next()){
                    lot = query.getString("cantidad");
                }
                
                if(lot == null){
                    JOptionPane.showMessageDialog(null, "No existe ese codigo en la base de datos","Error",0);
                    code.setText("");
                }else{
                    actualLot.setText(lot);
                    finalLot.setText(lot);
                    method.requestFocusInWindow();
                }
            } catch (SQLException ex) {
                Logger.getLogger(changeInventory.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_codeKeyPressed

    private void methodKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_methodKeyPressed
        if(evt.getKeyCode() == 10){
            newLot.requestFocusInWindow();
        }
    }//GEN-LAST:event_methodKeyPressed

