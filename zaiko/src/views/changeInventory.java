    private void methodItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_methodItemStateChanged
        switch (method.getSelectedIndex()) {
            case 0 -> {
                symbol.setText("+");
                newLotTitle.setText("Sumar cantidad:");
                hideFinalLot(true);
            }
            case 1 -> {
                symbol.setText("-");
                newLotTitle.setText("Restar cantidad:");
                hideFinalLot(true);
            }
            default -> {
                symbol.setText("=");
                newLotTitle.setText("Nueva cantidad:");
                hideFinalLot(false);
            }
        }
        newLot.setValue(0);
    }//GEN-LAST:event_methodItemStateChanged

    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        try {
            articleDB.updateLot(code.getText(), Integer.parseInt(finalLot.getText()));
        } catch (SQLException ex) {
            Logger.getLogger(changeInventory.class.getName()).log(Level.SEVERE, null, ex);
        }
        code.setText("");
        actualLot.setText("0");
        newLot.setValue(0);
        finalLot.setText("0");
        code.requestFocusInWindow();
    }//GEN-LAST:event_saveActionPerformed

